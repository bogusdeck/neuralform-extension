const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const envPath = path.join(repoRoot, '.env');
const outputPath = path.join(repoRoot, 'config', 'firebase-config.js');

const requiredKeys = [
    'FIREBASE_API_KEY',
    'FIREBASE_AUTH_DOMAIN',
    'FIREBASE_PROJECT_ID',
    'FIREBASE_STORAGE_BUCKET',
    'FIREBASE_MESSAGING_SENDER_ID',
    'FIREBASE_APP_ID'
];

function parseEnvFile(filePath) {
    if (!fs.existsSync(filePath)) {
        return {};
    }

    const env = {};
    const lines = fs.readFileSync(filePath, 'utf8').split(/\r?\n/);

    for (const line of lines) {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#')) {
            continue;
        }

        const separatorIndex = trimmed.indexOf('=');
        if (separatorIndex === -1) {
            continue;
        }

        const key = trimmed.slice(0, separatorIndex).trim();
        const value = trimmed.slice(separatorIndex + 1).trim();
        env[key] = value;
    }

    return env;
}

const env = parseEnvFile(envPath);
const firebaseConfig = {
    apiKey: env.FIREBASE_API_KEY || '',
    authDomain: env.FIREBASE_AUTH_DOMAIN || '',
    projectId: env.FIREBASE_PROJECT_ID || '',
    storageBucket: env.FIREBASE_STORAGE_BUCKET || '',
    messagingSenderId: env.FIREBASE_MESSAGING_SENDER_ID || '',
    appId: env.FIREBASE_APP_ID || ''
};

const missingKeys = requiredKeys.filter((key) => !env[key]);

const output = `// Generated by scripts/sync-firebase-config.js
globalThis.NEURALFORM_FIREBASE_CONFIG = ${JSON.stringify(firebaseConfig, null, 4)};
`;

fs.writeFileSync(outputPath, output, 'utf8');

if (missingKeys.length > 0) {
    console.warn(`Firebase config written with missing keys: ${missingKeys.join(', ')}`);
} else {
    console.log(`Firebase config synced to ${path.relative(repoRoot, outputPath)}`);
}
